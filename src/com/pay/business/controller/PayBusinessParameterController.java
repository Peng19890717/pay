    @RequestMapping("payBusinessParameter")
    public String getPayBusinessParameterList(HttpServletRequest request,HttpServletResponse response,
            @ModelAttribute("payBusinessParameter") PayBusinessParameter payBusinessParameter) {
        JWebUser user = (JWebUser)request.getSession().getAttribute("user");
        //登录和权限判断
        if(!JWebUserService.checkUser(user,request))return "/jsp/jweb/jump_login.jsp";
        //处理标识，空：跳转、0：列表
        String flag = request.getParameter("flag");
        new Blog(this.getClass().getSimpleName()+".getPayBusinessParameterList","取得PayBusinessParameter列表",user.id,request);
        OutputStream os = null;
        try {
            if(flag == null)return "/jsp/pay/business/pay_business_parameter.jsp";
            request.setCharacterEncoding("UTF-8");
            response.setContentType("text/html;charset=UTF-8");
            os = response.getOutputStream();
            int page = 1;
            int rows = JWebConstant.WEB_RECORD_COUNT_PER_PAGE;
            try {
                rows = Integer.parseInt((String)JWebConstant.SYS_CONFIG.get("WEB_RECORD_COUNT_PER_PAGE"));//系统配置的每页记录条数
            } catch (Exception e) {}
            try {
                page = Integer.parseInt(request.getParameter("page"));
            } catch (Exception e1) {}
            try {
                rows = Integer.parseInt(request.getParameter("rows"));//用户设置的每页记录条数
            } catch (Exception e1) {}
            os.write(new PayBusinessParameterService().getPayBusinessParameterList(payBusinessParameter,page,rows,
                request.getParameter("sort"),request.getParameter("order")).getBytes("utf-8"));
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if(os != null)try {os.close();} catch (IOException e1) {}
        }
        return null;
    }
    @RequestMapping("addPayBusinessParameter")
    public String addPayBusinessParameter(HttpServletRequest request,HttpServletResponse response,
            @ModelAttribute("payBusinessParameter") PayBusinessParameter payBusinessParameter) {
        JWebUser user = (JWebUser)request.getSession().getAttribute("user");
        OutputStream os = null;
        Blog log = null;
        try {
            os = response.getOutputStream();
            request.setCharacterEncoding("UTF-8");
            response.setContentType("text/html;charset=UTF-8");
            //登录和权限判断
            if(!JWebUserService.checkUser(user,request)){
               os.write(((String)request.getSession().getAttribute("LOGIN_INFO")).getBytes());
               return null;
            }
            log = new Blog(this.getClass().getSimpleName()+".addPayBusinessParameter","添加PayBusinessParameter",user.id,request);
            PayBusinessParameterService service=new PayBusinessParameterService();
            //判断业务参数是否存在
            if(service.existName(payBusinessParameter.name))throw new Exception("此变量已存在或系统保留变量");
            service.addPayBusinessParameter(payBusinessParameter);
            os.write(JWebConstant.OK.getBytes());
        } catch (Exception e) {
            e.printStackTrace();
            try {os.write(e.getMessage().getBytes("utf-8"));} catch (Exception e1) {}
            if(log!=null)log.info("添加失败,"+e.getMessage());
        } finally {
            if(os != null)try {os.close();} catch (IOException e1) {}
        }
        return null;
    }
